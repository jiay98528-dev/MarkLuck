/**
 * MarkLuck — 笔记本/文件系统相关类型定义
 *
 * 本文件定义笔记本实体、文件树节点、索引元数据及最近笔记的类型接口。
 * 与 spec/frontend/components.md 中同步定义保持一致，此处为权威源。
 */

// ============================================================================
// 辅助类型
// ============================================================================

/** 笔记相对路径（统一使用 / 分隔符） */
type NotePath = string;

/** 索引构建状态 */
type IndexStatus = 'idle' | 'scanning' | 'indexing' | 'ready' | 'error';

// ============================================================================
// Notebook — 笔记本实体
// ============================================================================

/**
 * 笔记本实体 —— 代表用户打开的一个本地文件夹（笔记本根目录）。
 *
 * 一个笔记本包含若干 .md 笔记文件和子文件夹（子笔记本）。
 * 所有数据存储在文件系统中，由 .markluck_index.json 维护索引。
 */
interface Notebook {
  /** 笔记本唯一标识（根路径的哈希值，用于快速索引查找） */
  id: string;

  /** 笔记本显示名称（取根文件夹名） */
  name: string;

  /** 笔记本根目录的绝对路径（平台相关，仅 Tauri 后端使用；Web 端使用相对路径） */
  path: string;

  /** 笔记本内的笔记文件路径列表（相对路径，如 "项目笔记/MarkLuck 架构设计.md"） */
  notes: NotePath[];

  /** 子文件夹路径列表（相对路径，如 "学习日记/", "_templates/"） */
  subfolders: string[];

  /** 索引系统状态 */
  indexStatus: IndexStatus;
}

// ============================================================================
// FileTreeNode — 文件树节点
// ============================================================================

/**
 * 文件树的统一节点类型，支持文件和文件夹。
 *
 * 在侧边栏文件树中递归渲染：文件夹节点可折叠/展开，文件节点可点击打开。
 */
interface FileTreeNode {
  /** 文件/文件夹名称（不含路径） */
  name: string;

  /** 相对路径（统一使用 / 分隔符，如 "项目笔记/技术选型.md"） */
  path: string;

  /** 节点类型 */
  type: 'file' | 'folder';

  /** 子节点（仅文件夹，递归结构） */
  children: FileTreeNode[];

  /** 文件夹展开状态（仅对文件夹有效，文件忽略此字段） */
  isOpen: boolean;
}

// ============================================================================
// NotebookMeta — .markluck_index.json 元数据
// ============================================================================

/**
 * 笔记本索引元数据，存储在笔记本根目录的 .markluck_index.json 中。
 *
 * 该文件维护标签、反向链接、全文索引等结构化元数据，
 * 避免每次启动时全量扫描所有 .md 文件。
 * 文件变更时增量更新，用户可手动触发"重建索引"。
 */
interface NotebookMeta {
  /** 索引格式版本（如 "1.0.0"），用于兼容性检查和迁移 */
  version: string;

  /** 最后一次索引时间（ISO 8601 字符串，如 "2026-06-03T10:30:00Z"） */
  lastIndexed: string;

  /** 已索引的 .md 文件总数 */
  fileCount: number;

  /** 已提取的唯一标签总数（合并 frontmatter tags 和行内 #tag） */
  tagCount: number;
}

// ============================================================================
// RecentNote — 最近笔记条目
// ============================================================================

/**
 * 最近编辑/打开过的笔记记录。
 *
 * 存储在 .markluck_index.json 的 `recentNotes` 字段中，
 * 用于"最近编辑"列表（RecentNotes 组件）快速访问近期工作。
 * 按 lastOpenedAt 倒序排列，默认显示最近 20 条。
 */
interface RecentNote {
  /** 笔记相对路径 */
  path: NotePath;

  /** 笔记标题（取自文件名或 H1 标题，用于列表展示） */
  title: string;

  /** 最后打开时间（Unix 毫秒时间戳） */
  lastOpenedAt: number;
}

// ============================================================================
// 导出聚合
// ============================================================================

export type { NotePath, IndexStatus };

export type { Notebook, FileTreeNode, NotebookMeta, RecentNote };
